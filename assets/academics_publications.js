// arg is    [string, list]
// return is [string, list]
recursiveAuthors = function(arg) {
    res = arg[0];
    list = arg[1];

    if (list.length == 1) {
        if (res.empty) {
            return [ list[0], [] ];
        }
        else {
            res += " and " + list[0];
            return [ res, [] ];
        }
    }
    else {
        res += list[0] + ", " ;
        list.shift();
        return recursiveAuthors([ res, list ]);
    }
}


Handlebars.registerHelper("authorList", function(list) {
    return recursiveAuthors([ "", list ])[0];
});

